

create table sales as (  
    select   
    SALESID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    LISTID NUMERIC(10, 2)  NULL,
    SELLERID NUMERIC(10, 2) NULL,
    BUYERID NUMERIC(10, 2) NULL,
    EVENTID NUMERIC(10, 2) NULL,
    DATEID NUMERIC(10, 2) NULL,
    QTYSOLD NUMERIC(10, 2) NULL,
    PRICEPAID NUMERIC(10, 2) NULL,
    COMMISSION NUMERIC(10, 2) NULL,
    SALETIME NUMERIC(10, 2) NULL,
    PRIMARY KEY ( SALESID )    
    FROM sales
)

